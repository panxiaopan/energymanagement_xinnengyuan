import initState from './initState.js'
const mutations = {
  updateStationList(state, newStationList) {
    state.stationList = newStationList
  },
  updateStationListConfig(state, newStationListConfig) {
    state.stationListConfig = newStationListConfig;
  },

  updateSocialUnit(state, newSocialUnit) {
    state.socialUnit = newSocialUnit;
  },
  updateDevice(state, newDevice) {
    state.device = newDevice;
  },
  updateActiveTabName(state, newActiveTabName) {
    state.activeTabName = newActiveTabName
  },
  resetState(state) {
    Object.assign(state, initState)
  }
}

export default mutations